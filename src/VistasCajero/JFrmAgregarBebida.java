    private void buttonAgregarBebidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAgregarBebidaMouseClicked

        int nobebi = Integer.parseInt(this.textAgregarBebida.getText());

        SistemaRestauranteFrames.listapedidos.get(JFrmEditarCuenta.noCuenta).getPedido().getBebida().add(
                new Bebida(JFrmCrearGerente.bebidas.get(nobebi).getNombre(), JFrmCrearGerente.bebidas.get(nobebi).getPrecio()));

        JOptionPane.showMessageDialog(null, "La bebida se ha agregado correctamente");
    }//GEN-LAST:event_buttonAgregarBebidaMouseClicked

