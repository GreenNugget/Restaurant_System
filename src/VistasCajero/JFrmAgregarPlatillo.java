    private void buttonAgregarPlatilloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAgregarPlatilloMouseClicked

        int noplat = Integer.parseInt(this.textAgregarPlatillo.getText());

        SistemaRestauranteFrames.listapedidos.get(JFrmEditarCuenta.noCuenta).getPedido().getPlatillo().add(
                new Platillo(JFrmCrearGerente.platillos.get(noplat).getNombre(), JFrmCrearGerente.platillos.get(noplat).getPrecio()));

        JOptionPane.showMessageDialog(null, "El platillo se ha agregado correctamente");
    }//GEN-LAST:event_buttonAgregarPlatilloMouseClicked

