    private void buttonEstablecerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonEstablecerMouseClicked

        String name = this.textNombreEmpleado.getText();
        String lastname1 = this.textApellidoPaterno.getText();
        String lastname2 = this.textApellidoMaterno.getText();
        double salary = Double.parseDouble(this.textSueldo.getText());

        SistemaRestauranteFrames.gerenteprincipal.establecerSueldoEmpleado(name, lastname2, lastname2, salary);

        JOptionPane.showMessageDialog(null, "Se ha establecido el sueldo "
                + "\ndel empleado correctamente");

        this.textNombreEmpleado.setText("");
        this.textApellidoPaterno.setText("");
        this.textApellidoMaterno.setText("");
        this.textSueldo.setText("");

    }//GEN-LAST:event_buttonEstablecerMouseClicked

