    private void imprimirRegistro() {

        File archivoRegistro = new File("RegistroVentas.txt");//Objeto de tipo archivo

        FileReader leerarchivo;//Variable para leer
        //Declaramos la variable de tipo String para recuperar las cadenas de texto del archivo
        String texto;

        try {

            //Creamos una instancia de tipo file reader
            leerarchivo = new FileReader(archivoRegistro);

            //Creamos una instancia de tipo buffer reader, el buffer es lo que almacena todo lo que se lee y no se pierda
            BufferedReader almacenamiento = new BufferedReader(leerarchivo);//Quiero que almacenes lo que vas leyendo

            //Inicializamos la variable de tipo String
            texto = "";

            //Creamos un ciclo para que se lea mientras sea distinto de null
            while (texto != null) {

                try {

                    //Le asignamos al almacenamiento para que guarde la l√≠nea de texto que lee
                    texto = almacenamiento.readLine();

                    if (texto != null) {
                        this.textAreaVisualizarRegistro.append(texto + "\n");
                    }

                } catch (IOException ex) {
                    Logger.getLogger(JFrmVerRegistro.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

            //CERRAMOS TODO FUERA DEL CICLO PARA QUE NO OCURRA UN ERROR
            try {

                //VAMOS A CERRAR EL BUFFER PRIMERO
                almacenamiento.close();
                //VAMOS A CERRAR EL ARCHIVO
                leerarchivo.close();

            } catch (IOException ex) {
                Logger.getLogger(JFrmVerRegistro.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrmVerRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

