    private void imprimirSueldos() {

        //Primero tenemos que leer el archivo y lo vamos imprimiendo simultaneamente
        File archivosueldos = new File("SueldoEmpleados.txt");//Objeto de tipo archivo

        FileReader leerarchivo;//Variable para leer

        String texto;

        try {

            leerarchivo = new FileReader(archivosueldos);

            BufferedReader almacenamiento = new BufferedReader(leerarchivo);

            texto = "";

            while (texto != null) {

                try {

                    texto = almacenamiento.readLine();

                    if (texto != null) {
                        this.textAreaVisualizarSueldos.append(texto + "\n");
                    }

                } catch (IOException ex) {
                    Logger.getLogger(JFrmVerSueldoEmpleado.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

            try {

                almacenamiento.close();
                leerarchivo.close();

            } catch (IOException ex) {
                Logger.getLogger(JFrmVerSueldoEmpleado.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrmVerSueldoEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        //FIN DEL MÃ‰TODO
    }

