    private void buttonAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAgregarMouseClicked

        int nopost = Integer.parseInt(this.textAgregarPostre.getText());

        SistemaRestauranteFrames.listapedidos.get(JFrmEditarCuenta.noCuenta).getPedido().getPostre().add(
                new Postre(JFrmCrearGerente.postres.get(nopost).getNombre(), JFrmCrearGerente.postres.get(nopost).getPrecio()));

        JOptionPane.showMessageDialog(null, "El postre se ha agregado correctamente");

    }//GEN-LAST:event_buttonAgregarMouseClicked

